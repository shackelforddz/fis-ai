function DonutChart({
  score,
  max,
  label,
}: {
  score: number;
  max: number;
  label: string;
}) {
  const percentage = (score / max) * 100;
  const data = [
    { name: "Score", value: percentage },
    { name: "Remaining", value: 100 - percentage },
  ];
  return (
    <div className="flex flex-col items-center gap-2">
      <h4 className="text-lg text-foreground self-start">{label}</h4>
      <div className="relative w-full aspect-square max-w-[320px]">
        <ResponsiveContainer width="100%" height="100%">
          <PieChart>
            <Pie
              data={data}
              cx="50%"
              cy="50%"
              innerRadius="65%"
              outerRadius="85%"
              startAngle={90}
              endAngle={-270}
              dataKey="value"
              stroke="none"
            >
              <Cell fill="#4bcd3e" />
              <Cell fill="rgba(255,255,255,0.1)" />
            </Pie>
          </PieChart>
        </ResponsiveContainer>
        <div className="absolute inset-0 flex items-center justify-center">
          <span className="text-5xl text-foreground font-bold">{score}</span>
        </div>
      </div>
    </div>
  );
}

function StatCard({
  label,
  value,
  subLabel,
}: {
  label: string;
  value: string;
  subLabel?: string;
}) {
  return (
    <div className="flex-1 flex flex-col gap-1 bg-gray-900 rounded-xl p-4">
      <div className="flex items-center justify-between text-xs text-muted-foreground">
        <span>{label}</span>
        <Info className="size-4 opacity-60" />
      </div>
      <span className="text-2xl text-foreground">{value}</span>
      {subLabel && <span className="text-xs text-muted-foreground">{subLabel}</span>}
    </div>
  );
}

function StatRow({
  items,
}: {
  items: { label: string; value: string; subLabel?: string }[];
}) {
  return (
    <div className="flex gap-6">
      {items.map((item) => (
        <StatCard key={item.label} {...item} />
      ))}
    </div>
  );
}

function StatusBadge({ status }: { status: "Complete" | "Draft" }) {
  if (status === "Complete") {
    return (
      <Badge className="bg-primary text-primary-foreground">{status}</Badge>
    );
  }
  return <Badge variant="outline">{status}</Badge>;
}

